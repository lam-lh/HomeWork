package Date20200306.HinhHoc;

public class HinhTron {
	public static final float SO_PI = 3.14f;

	/// Chu vi hình tròn
	public static double chuVi(double banKinh) {
		return banKinh * 2 * SO_PI;
	}

	/// Diện tích hình tròn
	public static double dienTich(double banKinh) {
		return banKinh * banKinh * SO_PI;

	}

}
