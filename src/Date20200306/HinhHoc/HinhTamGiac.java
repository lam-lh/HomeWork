package Date20200306.HinhHoc;

public class HinhTamGiac {

	/// Chu vi hình tam giác
	/// Số nguyên
	public static int chuVi(int a, int b, int c) {
		return (a + b + c);
	}

	public static int dienTich(int chieuCao, int canhDay) {
		return (chieuCao * canhDay)/2;
	}

	/// Số thực
	public static double chuVi(double a, double b, double d) {
		return (a + b + d);
	}

	public static double dienTich(double h, double d) {
		return (h * d)/2;
	}

}
