package Date20200306.HinhHoc;

public class HinhChuNhat {

///Chu vi hình vuông
	/// Số nguyên
	public static int chuVi(int a, int b) {
		return (a + b) * 2;
	}

	public static int dienTich(int a, int b) {
		return (a * b);
	}

	/// Số thực
	public static double chuVi(double a, double b) {
		return (a + b) * 2;
	}

	public static double dienTich(double a, double b) {
		return (a * b);
	}

}
